"""Request builder for Google Gemini API (AI Studio OpenAI-compatible chat completions)."""

from __future__ import annotations

from copy import deepcopy
from typing import Any, cast

from loguru import logger

from core.anthropic import ReasoningReplayMode, build_base_request_body
from core.anthropic.conversion import OpenAIConversionError
from providers.exceptions import InvalidRequestError


def _ensure_dict(container: dict[str, Any], key: str) -> dict[str, Any]:
    value = container.get(key)
    if isinstance(value, dict):
        return cast(dict[str, Any], value)
    nested: dict[str, Any] = {}
    container[key] = nested
    return nested


def _budget_to_effort(budget: int) -> str:
    """Map an explicit ``budget_tokens`` to a ``reasoning_effort`` string."""
    if budget <= 1024:
        return "minimal"
    if budget <= 2048:
        return "low"
    if budget <= 8192:
        return "medium"
    return "high"


def _apply_reasoning_effort(body: dict[str, Any], request_data: Any) -> None:
    """Set ``reasoning_effort`` based on the request's ``budget_tokens``.

    Maps Anthropic-style ``budget_tokens`` to OpenAI-standard ``reasoning_effort``
    values (minimal/low/medium/high), per Google's documented Gemini 3.x mappings.
    Defaults to ``"low"`` to reduce over-engineering for simple prompts.

    Must not be combined with ``thinking_config`` in ``extra_body`` -- they are
    mutually exclusive.  Without ``thinking_config``, ``include_thoughts`` defaults
    to off, keeping the model's internal reasoning invisible.
    """
    thinking_param = getattr(request_data, "thinking", None)
    budget: int | None = None
    if thinking_param is not None:
        b = getattr(thinking_param, "budget_tokens", None)
        if isinstance(b, int) and b > 0:
            budget = b

    if budget is not None:
        body["reasoning_effort"] = _budget_to_effort(budget)
    else:
        body["reasoning_effort"] = "low"


def _inject_thought_signatures(messages: list[dict[str, Any]]) -> None:
    """Add ``thought_signature`` to every tool call in conversation history.

    Gemini 3 models require ``thought_signature`` on every ``functionCall`` part
    when thinking is enabled.  The dummy string below skips validation for tool
    calls that were generated by a different model or replayed without their
    original signature (the documented escape hatch at
    https://ai.google.dev/gemini-api/docs/thought-signatures).
    """
    for msg in messages:
        tool_calls = msg.get("tool_calls")
        if not isinstance(tool_calls, list):
            continue
        for tc in tool_calls:
            if not isinstance(tc, dict):
                continue
            extra = _ensure_dict(tc, "extra_content")
            google = _ensure_dict(extra, "google")
            if "thought_signature" not in google:
                google["thought_signature"] = "skip_thought_signature_validator"


def build_request_body(request_data: Any, *, thinking_enabled: bool) -> dict:
    """Build OpenAI-format request body from an Anthropic request for Gemini."""
    logger.debug(
        "GEMINI_REQUEST: conversion start model={} msgs={}",
        getattr(request_data, "model", "?"),
        len(getattr(request_data, "messages", [])),
    )
    try:
        body = build_base_request_body(
            request_data,
            reasoning_replay=ReasoningReplayMode.REASONING_CONTENT
            if thinking_enabled
            else ReasoningReplayMode.DISABLED,
        )
    except OpenAIConversionError as exc:
        raise InvalidRequestError(str(exc)) from exc

    body["parallel_tool_calls"] = False

    extra_body: dict[str, Any] = {}
    request_extra = getattr(request_data, "extra_body", None)
    if isinstance(request_extra, dict):
        extra_body.update(deepcopy(request_extra))

    if thinking_enabled:
        # Gemini 3.x reasoning is optimised for default sampling settings;
        # temperature and top_p can interfere with reasoning.
        body.pop("temperature", None)
        body.pop("top_p", None)
        _apply_reasoning_effort(body, request_data)
        _inject_thought_signatures(body.get("messages", []))
    else:
        # Explicitly disable reasoning for models that default to it on
        # (Gemini 2.5+).  Gemini 3.x ignores this, but it's harmless.
        body["reasoning_effort"] = "none"

    if extra_body:
        body["extra_body"] = extra_body

    logger.debug(
        "GEMINI_REQUEST: conversion done model={} msgs={} tools={}",
        body.get("model"),
        len(body.get("messages", [])),
        len(body.get("tools", [])),
    )
    return body
